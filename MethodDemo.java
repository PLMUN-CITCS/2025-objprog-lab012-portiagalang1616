// Define the class
public class MethodDemo {  

    // Main method to execute the program
    public static void main(String[] args) {
        greet();  // Calling the greet method
        printMessage("This is my message!"); // Calling the printMessage method
    }

    // Define the greet method (Part 1)
    public static void greet() {
        System.out.println("Hello from the greet method!"); // greet method (Part 2)
    }

    // Define the printMessage method (Part 1)
    public static void printMessage(String message) {
        System.out.println(message); // printMessage method (Part 2)
    }

} // End of class
